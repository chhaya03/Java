public class operators {

    public static void main(String[] args) {

        int a = 4;
        int b = 6 + a; // = is assignment operator
        System.out.println(b);

        double d = 50;
        double c = 115 % d;
        System.out.println(c);
        System.out.println(10 > 15); // comparison Operators
        System.out.println(65>30 && 10<50);

    }

}
