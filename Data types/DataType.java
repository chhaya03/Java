public class DataType {

    public static void main(String[] args) {

        int x = 5;
        int y = 10;

        int a = x + y;
        float b = 6.54f + x;
        System.out.println(a);
        System.out.println(b);

        // Increment and Decrement Operators

        int i = 56;
        System.out.println(i++);
        System.out.println(i);
        System.out.println(++i);
        System.out.println(i);

        int j = 7;
        System.out.println(++j * 8);

        char ch = 'A';

        System.out.println(++ch);
    }
}
